package BASICS;

public class If_Else {
    public static void main(String[] args) {
        int x  = 3;
        if (x % 2 == 0) {
            System.out.println(x + " is even");
        } else {
            System.out.println(x + " is odd");
        }

        System.out.println("Smart work is better then hardwork");

    }
}